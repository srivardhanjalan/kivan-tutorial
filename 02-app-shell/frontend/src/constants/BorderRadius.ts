/**
 * Standard border radius values for consistent rounded corners across the app.
 * These values follow a design system for visual consistency.
 */

export const BorderRadius = {
  /**
   * Extra small radius - 8px
   * Use for: Small buttons, tags, badges
   */
  xs: 8,

  /**
   * Small radius - 12px
   * Use for: Compact cards, chips, small containers
   */
  sm: 12,

  /**
   * Medium radius - 16px
   * Use for: Standard cards, inputs, modals
   */
  md: 16,

  /**
   * Large radius - 20px
   * Use for: Large cards, modal content, prominent containers
   */
  lg: 20,

  /**
   * Extra large radius - 24px
   * Use for: Hero cards, feature cards, main content areas
   */
  xl: 24,

  /**
   * 2X extra large radius - 28px
   * Use for: Wish cards, prominent feature cards
   */
  xxl: 28,

  /**
   * Pill/Circular radius - 30px (for height: 60)
   * Use for: Tab buttons, pill buttons, circular elements
   */
  pill: 30,
};

export default BorderRadius;
